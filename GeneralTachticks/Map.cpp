Map::Map()
{
	mDirt = TextureManager::LoadTexture("Assets/terrain/dirt_normal.png");
	mGrass = TextureManager::LoadTexture("Assets/terrain/rock_normal.png");
	mWater = TextureManager::LoadTexture("Assets/terrain/water_normal.png");

	LoadMap(lvl1);

	mSrc.x = mSrc.y = 0;
	mSrc.w = 100;
	mSrc.h = 116;
	mDest.w = 64;
	mDest.h = 64;
	mDest.x = mDest.y = 0;
}

Map::~Map()
{}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			mMap[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = mMap[row][column];

			mDest.x = column * 64 + ((row%2)*32);
			mDest.y = row * 64 - (row*16);

			switch (type)
			{
			case 0:
				TextureManager::Draw(mDirt, mSrc, mDest);
				break;

			case 1:
				TextureManager::Draw(mGrass, mSrc, mDest);
				break;

			case 2:
				TextureManager::Draw(mWater, mSrc, mDest);
				break;

			default:
				break;
			}
		}
	}
}